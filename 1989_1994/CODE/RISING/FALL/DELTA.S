DELTA_PACK:
	MOVEQ	#0,D7
	MOVEQ	#0,D6
	MOVE.L	A6,A5
	ADDQ	#2,A6
.IDEM	ADDQ	#2,D7
	CMPI	#32000,D7
	BEQ.S	.FINI
	MOVE	(A0)+,D0
	BEQ.S	.DEPART_VIDE
	MOVE	(A1)+,D1
	BEQ.S	.FIN_VIDE
	CMP	D0,D1
	BEQ.S	.IDEM
	;PAS OUBLIER TEST DE FIN
	ADDQ	#1,D6
	MOVE	D1,(A6)+
	MOVE	D7,(A6)+
	BRA.S	.IDEM	
.DEPART_VIDE	MOVE	(A1)+,D1
	BEQ.S	.IDEM
	;
	ADDQ	#1,D6
	MOVE	D1,(A6)+
	MOVE	D7,(A6)+
	BRA.S	.IDEM
.FIN_VIDE	;ET DEPART PLEIN=>CLR
	CLR	(A6)+
	MOVE	D7,(A6)+
	ADDQ	#1,D6
	BRA.S	.IDEM
.FINI	SUBQ	#1,D6
	MOVE	D6,(A5)